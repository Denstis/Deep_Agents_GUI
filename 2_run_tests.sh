#!/bin/bash
# Deep Agents GUI - Run Tests Script for macOS/Linux

set +e

echo "============================================================"
echo "  Deep Agents GUI: Проверка системы (Тесты)"
echo "============================================================"
echo ""

# Проверка наличия venv
if [ ! -f "venv/bin/activate" ]; then
    echo "[ОШИБКА] Виртуальное окружение не найдено!"
    echo "Сначала запустите 'bash 1_install.sh'."
    exit 1
fi

# Активация окружения
source venv/bin/activate

echo "[INFO] Запуск модульных тестов (Tools Layer)..."
echo "------------------------------------------------------------"
python -m pytest tests/test_tools.py -v --tb=short
TOOLS_EXIT=$?

echo ""
echo "------------------------------------------------------------"
echo "[INFO] Запуск интеграционных тестов (GUI Integration)..."
echo "------------------------------------------------------------"
python -m pytest tests/test_gui_integration.py -v --tb=short
GUI_EXIT=$?

echo ""
echo "============================================================"
echo "  Результаты тестирования"
echo "============================================================"
echo ""

if [ $TOOLS_EXIT -eq 0 ]; then
    echo "[OK] Модульные тесты: ПРОЙДЕНЫ"
else
    echo "[FAIL] Модульные тесты: ОШИБКИ"
fi

if [ $GUI_EXIT -eq 0 ]; then
    echo "[OK] Интеграционные тесты: ПРОЙДЕНЫ"
else
    echo "[FAIL] Интеграционные тесты: ОШИБКИ"
fi

echo ""

if [ $TOOLS_EXIT -eq 0 ] && [ $GUI_EXIT -eq 0 ]; then
    echo "============================================================"
    echo "  ВСЕ ТЕСТЫ ПРОЙДЕНЫ УСПЕШНО! Система готова к работе."
    echo "============================================================"
    echo ""
    echo "Следующий шаг:"
    echo "1. Создайте файл .env с вашим API ключом (см. .env.example)"
    echo "2. Запустите 'bash 4_launch_gui.sh'"
else
    echo "============================================================"
    echo "  ОБНАРУЖЕНЫ ОШИБКИ! Проверьте логи выше."
    echo "============================================================"
    echo ""
    echo "Возможные причины:"
    echo "- Не установлены зависимости (запустите 1_install.sh)"
    echo "- Ошибки в коде инструментов или графа"
    echo "- Проблемы с совместимостью версий библиотек"
fi

echo ""
